const SITE_ADMIN_URL = process.env['NEXT_PUBLIC_ADMIN_URL'] ?? 'http://localhost:3004';

// Static — derived once from the constant NAV_ITEMS array
const NAV_SECTIONS = Array.from(new Set(NAV_ITEMS.map((i) => i.section ?? '')));

const SidebarContent = memo(function SidebarContent({
  pathname,
  onLogout,
  onClose,
}: {
  pathname: string;
  onLogout: () => void;
  onClose?: () => void;
}) {
  const sections = NAV_SECTIONS;

  // Auto-open the section containing the active route; open all by default
  const activeSection = NAV_ITEMS.find((i) => pathname.startsWith(i.href))?.section ?? '';
  const [openSections, setOpenSections] = useState<Set<string>>(
    () => new Set(sections),
  );
  const initialized = useRef(false);

  // Ensure active section is open when navigating
  useEffect(() => {
    if (!initialized.current) { initialized.current = true; return; }
    if (activeSection) setOpenSections((prev) => new Set([...prev, activeSection]));
  }, [activeSection]);

  function toggle(s: string) {
    setOpenSections((prev) => {
      const next = new Set(prev);
      if (next.has(s)) next.delete(s);
      else next.add(s);
      return next;
    });
  }

  return (
    <>
      <div className="px-5 py-5 border-b border-white/10">
        <span className="text-white font-bold text-lg tracking-tight">haritailesi</span>
        <span className="block text-white/50 text-xs mt-0.5">Yönetim Paneli</span>
      </div>

      <nav className="flex-1 px-3 py-4 overflow-y-auto">
        {sections.map((section) => {
          const items = NAV_ITEMS.filter((i) => (i.section ?? '') === section);
          const isOpen = openSections.has(section);
          const hasActive = items.some((i) => pathname.startsWith(i.href));

          return (
            <div key={section} className="mb-1">
              {section ? (
                <button
                  onClick={() => toggle(section)}
                  className={`w-full flex items-center justify-between px-3 py-2 rounded-lg text-left transition-colors ${
                    hasActive ? 'text-white/90' : 'text-white/40 hover:text-white/60'
                  }`}
                >
                  <span className="text-[10px] font-bold uppercase tracking-widest">{section}</span>
                  <svg
                    className={`w-3 h-3 transition-transform duration-200 ${isOpen ? 'rotate-180' : ''}`}
                    fill="none" stroke="currentColor" viewBox="0 0 24 24"
                  >
                    <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M19 9l-7 7-7-7" />
                  </svg>
                </button>
              ) : null}

              {isOpen && (
                <div className="space-y-0.5 mb-2">
                  {items.map((item) => {
                    const active = pathname.startsWith(item.href);
                    return (
                      <Link
                        key={item.href}
                        href={item.href}
                        {...(onClose ? { onClick: onClose } : {})}
                        className={`flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors border-l-[3px] ${
                          active
                            ? 'border-[#66aca9] bg-white/15 text-white'
                            : 'border-transparent text-white/65 hover:bg-white/10 hover:text-white'
                        }`}
                      >
                        {item.icon}
                        {item.label}
                      </Link>
                    );
                  })}
                </div>
              )}
            </div>
          );
        })}
      </nav>

      <div className="px-3 py-4 border-t border-white/10 space-y-1">
        <a
          href={SITE_ADMIN_URL}
          target="_blank"
          rel="noopener noreferrer"
          className="flex items-center gap-3 w-full px-3 py-2 rounded-lg text-sm font-medium text-white/50 hover:bg-white/10 hover:text-white/80 transition-colors"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
              d="M10 6H6a2 2 0 00-2 2v10a2 2 0 002 2h10a2 2 0 002-2v-4M14 4h6m0 0v6m0-6L10 14" />
          </svg>
          Vakıf Site Admin ↗
        </a>
        <button
          onClick={onLogout}
          className="flex items-center gap-3 w-full px-3 py-2 rounded-lg text-sm font-medium text-white/70 hover:bg-white/10 hover:text-white transition-colors"
        >
          <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2}
              d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Çıkış Yap
        </button>
      </div>
    </>
  );
});

export default function PanelLayout({ children }: { children: React.ReactNode }) {
  const router = useRouter();
  const pathname = usePathname();
  const [sidebarOpen, setSidebarOpen] = useState(false);

  useEffect(() => {
    if (typeof window !== 'undefined' && !localStorage.getItem('access_token')) {
      router.replace('/login');
    }
  }, [router]);

  useEffect(() => {
    setSidebarOpen(false);
  }, [pathname]);

  const handleLogout = useCallback(() => {
    localStorage.removeItem('access_token');
    localStorage.removeItem('refresh_token');
    router.replace('/login');
  }, [router]);

  return (
    <div className="min-h-screen bg-gray-50 flex">
      {/* Desktop sidebar */}
      <aside className="hidden md:flex w-56 bg-[var(--color-mavi)] flex-col shrink-0">
        <SidebarContent pathname={pathname} onLogout={handleLogout} />
      </aside>

      {/* Mobile drawer */}
      {sidebarOpen && (
        <div className="md:hidden fixed inset-0 z-40 flex">
          <div
            className="fixed inset-0 bg-black/50"
            onClick={() => setSidebarOpen(false)}
          />
          <aside className="relative z-50 w-64 bg-[var(--color-mavi)] flex flex-col h-full">
            <SidebarContent
              pathname={pathname}
              onLogout={handleLogout}
              onClose={() => setSidebarOpen(false)}
            />
          </aside>
        </div>
      )}

      {/* Main */}
      <div className="flex-1 flex flex-col overflow-hidden">
        {/* Mobile top bar */}
        <header className="md:hidden flex items-center gap-3 bg-[var(--color-mavi)] px-4 py-3 shrink-0">
          <button
            onClick={() => setSidebarOpen(true)}
            className="text-white/80 hover:text-white"
            aria-label="Menüyü aç"
          >
            <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
            </svg>
          </button>
          <span className="text-white font-bold text-base tracking-tight">haritailesi</span>
          <span className="text-white/50 text-xs">Mutfak Admin</span>
        </header>

        <main className="flex-1 overflow-auto">
          <div className="max-w-6xl mx-auto px-4 sm:px-6 py-6 sm:py-8">
            {children}
          </div>
        </main>
      </div>
    </div>
  );
}
